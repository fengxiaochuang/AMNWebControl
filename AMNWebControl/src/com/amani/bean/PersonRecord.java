package com.amani.bean;

public class PersonRecord {
	private int id;
	private int personID;
	private int stat;
	private String time;
	private int workTyte;
	private String ddaterecord;
	private String ttimerecord;
	public String getDdaterecord() {
		return ddaterecord;
	}
	public void setDdaterecord(String ddaterecord) {
		this.ddaterecord = ddaterecord;
	}
	public String getTtimerecord() {
		return ttimerecord;
	}
	public void setTtimerecord(String ttimerecord) {
		this.ttimerecord = ttimerecord;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getPersonID() {
		return personID;
	}
	public void setPersonID(int personID) {
		this.personID = personID;
	}
	public int getStat() {
		return stat;
	}
	public void setStat(int stat) {
		this.stat = stat;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public int getWorkTyte() {
		return workTyte;
	}
	public void setWorkTyte(int workTyte) {
		this.workTyte = workTyte;
	}
	
}
